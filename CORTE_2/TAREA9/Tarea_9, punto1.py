def ordenar_matriz(matriz):
    filas = len(matriz)
    columnas = len(matriz[0])

    for i in range(filas):
        for j in range(columnas):
            max_fila = i
            max_columna = j


            for x in range(i, filas):
                for y in range(j, columnas):
                    if matriz[x][y] > matriz[max_fila][max_columna]:
                        max_fila = x
                        max_columna = y


            matriz[i][j], matriz[max_fila][max_columna] = matriz[max_fila][max_columna], matriz[i][j]


def main():
    matriz = crear_matriz()
    print("Matriz original:")
    imprimir(matriz)
    max_min(matriz)
    ordenar_matriz(matriz)

    print("\nMatriz ordenada de mayor a menor:")
    imprimir(matriz)

if __name__ == "__main__":
    main()

